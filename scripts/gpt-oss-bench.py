"""Benchmark gpt-oss with TokenSpeed's in-process Engine API.

This mirrors the random workload used by ``tokenspeed bench`` while bypassing
the SMG/OpenAI HTTP gateway.  It submits ``input_ids`` directly to ``Engine`` so
the engine sees exactly the token lengths generated by ``RandomDataset``.

Equivalent serving benchmark shape:

    tokenspeed bench \
      --backend tokenspeed \
      --host 127.0.0.1 \
      --port 21000 \
      --model amd/gpt-oss-120b-w-mxfp4-a-fp8 \
      --dataset-name random \
      --random-input-len 8192 \
      --random-output-len 1024 \
      --random-range-ratio 0.0 \
      --num-prompts 80 \
      --max-concurrency 8 \
      --request-rate inf \
      --seed 1

Edit ``BenchmarkConfig`` below to change the hardcoded benchmark values.
"""

from __future__ import annotations

import asyncio
import time
import traceback
from dataclasses import dataclass
from typing import Any

import numpy as np

from tokenspeed.bench import (
    RandomDataset,
    RequestFuncOutput,
    calculate_metrics,
    get_tokenizer,
)
from tokenspeed.runtime.entrypoints.engine import Engine
from tokenspeed.runtime.utils.server_args import prepare_server_args


@dataclass
class BenchmarkConfig:
    model: str = "amd/gpt-oss-120b-w-mxfp4-a-fp8"
    tokenizer: str | None = None
    host: str = "127.0.0.1"
    port: int = 21000
    world_size: int = 1
    random_input_len: int = 8192
    random_output_len: int = 1024
    random_range_ratio: float = 0.0
    random_prefix_len: int = 0
    num_prompts: int = 80
    max_concurrency: int = 8
    seed: int = 1
    request_id_prefix: str = "bench-"
    num_warmups: int = 10
    disable_kvstore: bool = True
    disable_prefix_caching: bool = True
    enforce_eager: bool = False
    use_text_prompts: bool = False


def _print_section_header(title: str, fill: str) -> None:
    print(f"{title:{fill}^50}")


def _print_metric_row(label: str, value: Any, precision: int | None = None) -> None:
    formatted_value = (
        f"{value:<10}" if precision is None else f"{value:<10.{precision}f}"
    )
    print(f"{label:<40} {formatted_value}")


def build_server_argv(config: BenchmarkConfig) -> list[str]:
    argv = [
        "--model",
        config.model,
        "--host",
        config.host,
        "--port",
        str(config.port),
        "--world-size",
        str(config.world_size),
    ]
    if config.disable_kvstore:
        argv.append("--disable-kvstore")
    if config.disable_prefix_caching:
        argv.append("--no-enable-prefix-caching")
    if config.enforce_eager:
        argv.append("--enforce-eager")
    return argv


def build_requests(config: BenchmarkConfig):
    tokenizer = get_tokenizer(config.tokenizer or config.model)
    requests = RandomDataset(random_seed=config.seed).sample(
        tokenizer=tokenizer,
        num_requests=config.num_prompts,
        request_id_prefix=config.request_id_prefix,
        input_len=config.random_input_len,
        output_len=config.random_output_len,
        range_ratio=config.random_range_ratio,
        prefix_len=config.random_prefix_len,
    )
    print(f"#Input tokens: {sum(request.prompt_len for request in requests)}")
    print(
        f"#Output tokens: "
        f"{sum(request.expected_output_len for request in requests)}"
    )
    return tokenizer, requests


def encode_prompt(tokenizer, request) -> list[int]:
    input_ids = tokenizer.encode(request.prompt, add_special_tokens=False)
    if len(input_ids) != request.prompt_len:
        raise ValueError(
            f"request {request.request_id} prompt length mismatch: "
            f"dataset={request.prompt_len}, encoded={len(input_ids)}"
        )
    return input_ids


async def run_one(
    engine: Engine,
    tokenizer,
    request,
    sem: asyncio.Semaphore,
    *,
    use_text_prompts: bool,
) -> RequestFuncOutput:
    async with sem:
        output = RequestFuncOutput(prompt_len=request.prompt_len)
        generated_text = ""
        completion_tokens = 0
        first_chunk = True
        start = time.perf_counter()
        output.start_time = start
        most_recent_timestamp = start

        try:
            sampling_params = {
                "max_new_tokens": request.expected_output_len,
                "ignore_eos": True,
                "repetition_penalty": 1.0,
            }
            if use_text_prompts:
                stream = await engine.async_generate(
                    prompt=request.prompt,
                    sampling_params=sampling_params,
                    stream=True,
                    user_rid=request.request_id,
                )
            else:
                stream = await engine.async_generate(
                    input_ids=encode_prompt(tokenizer, request),
                    sampling_params=sampling_params,
                    stream=True,
                    user_rid=request.request_id,
                )

            async for chunk in stream:
                timestamp = time.perf_counter()
                if first_chunk:
                    first_chunk = False
                    output.ttft = timestamp - start
                else:
                    output.itl.append(timestamp - most_recent_timestamp)
                most_recent_timestamp = timestamp

                text = chunk.get("text")
                if text:
                    generated_text = text
                meta_info = chunk.get("meta_info") or {}
                if meta_info.get("completion_tokens") is not None:
                    completion_tokens = int(meta_info["completion_tokens"])

            if first_chunk:
                output.success = False
                output.error = "Never received a valid chunk to calculate TTFT."
            else:
                output.success = True
                output.output_tokens = completion_tokens or len(output.itl) + 1
                output.generated_text = generated_text
                output.latency = most_recent_timestamp - start
        except Exception:  # noqa: BLE001
            output.success = False
            output.latency = time.perf_counter() - start
            output.error = traceback.format_exc()
        return output


async def run_requests(
    engine: Engine,
    tokenizer,
    requests,
    *,
    max_concurrency: int,
    use_text_prompts: bool,
) -> list[RequestFuncOutput]:
    sem = asyncio.Semaphore(max_concurrency)
    tasks = [
        asyncio.create_task(
            run_one(
                engine,
                tokenizer,
                request,
                sem,
                use_text_prompts=use_text_prompts,
            )
        )
        for request in requests
    ]
    return await asyncio.gather(*tasks)


def print_summary(
    input_requests,
    outputs: list[RequestFuncOutput],
    duration: float,
    tokenizer,
    max_concurrency: int,
) -> None:
    metrics, _ = calculate_metrics(
        input_requests,
        outputs,
        duration,
        tokenizer,
        selected_percentiles=[99.0],
        goodput_config_dict={},
    )

    _print_section_header(" Serving Benchmark Result ", "=")
    _print_metric_row("Successful requests:", metrics.completed)
    _print_metric_row("Failed requests:", metrics.failed)
    _print_metric_row("Maximum request concurrency:", max_concurrency)
    _print_metric_row("Benchmark duration (s):", duration, precision=2)
    _print_metric_row("Total input tokens:", metrics.total_input)
    _print_metric_row("Total generated tokens:", metrics.total_output)
    _print_metric_row(
        "Request throughput (req/s):", metrics.request_throughput, precision=2
    )
    _print_metric_row(
        "Output token throughput (tok/s):", metrics.output_throughput, precision=2
    )
    _print_metric_row(
        "Peak output token throughput (tok/s):",
        metrics.max_output_tokens_per_s,
        precision=2,
    )
    _print_metric_row(
        "Peak concurrent requests:", metrics.max_concurrent_requests, precision=2
    )
    _print_metric_row(
        "Total token throughput (tok/s):", metrics.total_token_throughput, precision=2
    )
    _print_section_header("Time to First Token", "-")
    _print_metric_row("Mean TTFT (ms):", metrics.mean_ttft_ms, precision=2)
    _print_metric_row("Median TTFT (ms):", metrics.median_ttft_ms, precision=2)
    _print_metric_row("P99 TTFT (ms):", metrics.percentiles_ttft_ms[0][1], precision=2)
    _print_section_header("Time per Output Token (excl. 1st token)", "-")
    _print_metric_row("Mean TPOT (ms):", metrics.mean_tpot_ms, precision=2)
    _print_metric_row("Median TPOT (ms):", metrics.median_tpot_ms, precision=2)
    _print_metric_row("P99 TPOT (ms):", metrics.percentiles_tpot_ms[0][1], precision=2)
    _print_section_header("Inter-token Latency", "-")
    _print_metric_row("Mean ITL (ms):", metrics.mean_itl_ms, precision=2)
    _print_metric_row("Median ITL (ms):", metrics.median_itl_ms, precision=2)
    _print_metric_row("P99 ITL (ms):", metrics.percentiles_itl_ms[0][1], precision=2)
    print("=" * 50)


async def main() -> None:
    config = BenchmarkConfig()
    if config.max_concurrency <= 0:
        raise ValueError("max_concurrency must be positive")

    np.random.seed(config.seed)
    server_argv = build_server_argv(config)
    print("Engine args:", " ".join(server_argv))
    tokenizer, input_requests = build_requests(config)

    engine = Engine(server_args=prepare_server_args(server_argv))
    try:
        warmup_count = min(config.num_warmups, len(input_requests))
        if warmup_count > 0:
            print(f"Running {warmup_count} warmup request(s)...")
            await run_requests(
                engine,
                tokenizer,
                input_requests[:warmup_count],
                max_concurrency=min(config.max_concurrency, warmup_count),
                use_text_prompts=config.use_text_prompts,
            )

        print(
            f"Running {len(input_requests)} benchmark requests "
            f"with max_concurrency={config.max_concurrency} "
            f"using {'text prompts' if config.use_text_prompts else 'input_ids'}..."
        )
        start = time.perf_counter()
        outputs = await run_requests(
            engine,
            tokenizer,
            input_requests,
            max_concurrency=config.max_concurrency,
            use_text_prompts=config.use_text_prompts,
        )
        duration = time.perf_counter() - start
        print_summary(
            input_requests, outputs, duration, tokenizer, config.max_concurrency
        )
    finally:
        engine.shutdown()


if __name__ == "__main__":
    asyncio.run(main())
